/* Databases exam quiz — vanilla JS, no runtime build.
   Data from window.QUIZ_DATA (generated by scripts/build.py). */
(function () {
  "use strict";

  var DATA = window.QUIZ_DATA || { exams: [], topics: [], questions: [], presentations: {} };
  var PRES = DATA.presentations || {};
  var $ = function (id) { return document.getElementById(id); };

  // ---- safe inline markdown ----
  function esc(s) { return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;"); }
  function inline(s) {
    return esc(s)
      .replace(/`([^`]+)`/g, "<code>$1</code>")
      .replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
      .replace(/(^|[^*])\*([^*]+)\*/g, "$1<em>$2</em>");
  }
  function md(s) {
    if (!s) return "";
    return s.split(/\n\s*\n/).map(function (p) {
      return "<p>" + inline(p.trim()).replace(/\n/g, "<br>") + "</p>";
    }).join("");
  }
  function shuffle(a) {
    for (var i = a.length - 1; i > 0; i--) {
      var j = Math.floor(Math.random() * (i + 1));
      var t = a[i]; a[i] = a[j]; a[j] = t;
    }
    return a;
  }
  function sameSet(a, b) {
    if (a.length !== b.length) return false;
    return a.slice().sort().join("|") === b.slice().sort().join("|");
  }
  function examLabel(id) {
    var e = DATA.exams.filter(function (x) { return x.id === id; })[0];
    return e ? e.label : id;
  }

  // ---------- stats (localStorage, per-question, recency-weighted) ----------
  var STORE_KEY = "db_quiz_stats_v1";
  // Tweak the grading here: how strongly recent attempts dominate older ones.
  var SCORING = { qDecay: 0.55, topicDecay: 0.6, weakThreshold: 0.7, attemptCap: 25 };

  function loadStats() {
    try { return JSON.parse(localStorage.getItem(STORE_KEY)) || { questions: {} }; }
    catch (e) { return { questions: {} }; }
  }
  function saveStats() {
    try { localStorage.setItem(STORE_KEY, JSON.stringify(STATS)); } catch (e) {}
  }
  var STATS = loadStats();

  function recordAttempt(qid, ok) {
    var rec = STATS.questions[qid] || (STATS.questions[qid] = { attempts: [] });
    rec.attempts.push({ ts: Date.now(), run: currentRun, ok: ok ? 1 : 0 });
    if (rec.attempts.length > SCORING.attemptCap) rec.attempts.shift();
    saveStats();
  }

  // recency-weighted score in [0,1] for one question; null if never attempted
  function qScore(qid) {
    var rec = STATS.questions[qid];
    if (!rec || !rec.attempts.length) return null;
    var a = rec.attempts.slice().sort(function (x, y) { return x.ts - y.ts; });
    var num = 0, den = 0, w = 1;
    for (var i = a.length - 1; i >= 0; i--) { num += w * a[i].ok; den += w; w *= SCORING.qDecay; }
    return den ? num / den : null;
  }
  function qLastTs(qid) {
    var rec = STATS.questions[qid];
    if (!rec || !rec.attempts.length) return 0;
    return rec.attempts.reduce(function (m, x) { return x.ts > m ? x.ts : m; }, 0);
  }

  // topic score: recency-weighted across the topic's attempted questions
  function topicStat(slug) {
    var qs = DATA.questions.filter(function (q) {
      return q.type !== "open" && (q.pdfs || []).indexOf(slug) !== -1;
    });
    var attempted = qs.filter(function (q) { return qScore(q.id) !== null; });
    if (!attempted.length) return { score: null, tried: 0, total: qs.length };
    attempted.sort(function (a, b) { return qLastTs(b.id) - qLastTs(a.id); });
    var num = 0, den = 0, w = 1;
    attempted.forEach(function (q) { num += w * qScore(q.id); den += w; w *= SCORING.topicDecay; });
    return { score: den ? num / den : null, tried: attempted.length, total: qs.length };
  }
  function overallStat() {
    var ids = Object.keys(STATS.questions);
    var scored = ids.map(function (id) { return { s: qScore(id), ts: qLastTs(id) }; })
      .filter(function (x) { return x.s !== null; });
    scored.sort(function (a, b) { return b.ts - a.ts; });
    var num = 0, den = 0, w = 1;
    scored.forEach(function (x) { num += w * x.s; den += w; w *= SCORING.topicDecay; });
    var attempts = ids.reduce(function (n, id) { return n + STATS.questions[id].attempts.length; }, 0);
    return { score: den ? num / den : null, tried: scored.length, attempts: attempts };
  }
  function grade10(score) { return score === null ? null : Math.round((1 + 9 * score) * 10) / 10; }
  function gradeClass(g) { return g === null ? "" : (g >= 7 ? "g-good" : (g >= 4 ? "g-mid" : "g-bad")); }

  // weakest-first practice quiz; scope = topic slug or null (everything)
  function buildWeakQuiz(scope) {
    var pool = DATA.questions.filter(function (q) {
      return q.type !== "open" && (!scope || (q.pdfs || []).indexOf(scope) !== -1);
    }).map(function (q) { return { q: q, s: qScore(q.id) }; });
    var weak = pool.filter(function (x) { return x.s !== null && x.s < SCORING.weakThreshold; })
      .sort(function (a, b) { return a.s - b.s; });
    var rest = shuffle(pool.filter(function (x) { return weak.indexOf(x) === -1; }));
    var ordered = weak.concat(rest).map(function (x) { return x.q; });
    return ordered.slice(0, Math.min(20, ordered.length));
  }

  // ---------- state ----------
  var mode = "year";
  var selYear = {};
  var selTopic = {};
  var quiz = [], openStudy = [], idx = 0, answers = [], locked = false, currentRun = null;

  // ---------- start screen ----------
  function renderStart() {
    var list = $("select-list");
    list.innerHTML = "";

    if (mode === "year") {
      $("select-hint").textContent = "Pick one or more past exams.";
      if (!DATA.exams.length) { list.innerHTML = '<p class="empty">No exams found.</p>'; }
      DATA.exams.forEach(function (ex) {
        if (ex.closed + ex.open === 0) return;
        var meta = ex.closed + " Q" + (ex.open ? " · " + ex.open + " open" : "");
        list.appendChild(optRow(ex.id, ex.label, meta, selYear));
      });
    } else {
      $("select-hint").textContent = "Pick one or more topics (lecture presentations).";
      if (!DATA.topics.length) { list.innerHTML = '<p class="empty">No topics found.</p>'; }
      DATA.topics.forEach(function (t) {
        var meta = t.closed + " Q" + (t.open ? " · " + t.open + " open" : "");
        list.appendChild(optRow(t.slug, t.name, meta, selTopic));
      });
    }
    updateStart();
  }

  function optRow(key, label, meta, store) {
    var row = document.createElement("label");
    row.className = "opt-row" + (store[key] ? " sel" : "");
    row.innerHTML =
      '<input type="checkbox" ' + (store[key] ? "checked" : "") + ">" +
      '<span class="lbl">' + esc(label) + "</span>" +
      '<span class="meta">' + esc(meta) + "</span>";
    var cb = row.querySelector("input");
    cb.addEventListener("change", function () {
      store[key] = cb.checked;
      row.classList.toggle("sel", cb.checked);
      updateStart();
    });
    return row;
  }

  function currentStore() { return mode === "year" ? selYear : selTopic; }
  function selectedKeys() {
    var s = currentStore();
    return Object.keys(s).filter(function (k) { return s[k]; });
  }
  function poolFor(keys) {
    if (mode === "year") {
      return DATA.questions.filter(function (q) {
        return keys.indexOf(q.exam) !== -1;
      });
    }
    return DATA.questions.filter(function (q) {
      return (q.pdfs || []).some(function (p) { return keys.indexOf(p) !== -1; });
    });
  }
  function updateStart() {
    var keys = selectedKeys();
    var all = poolFor(keys);
    var closed = all.filter(function (q) { return q.type !== "open"; }).length;
    $("start-btn").disabled = closed === 0;
    $("sel-count").textContent = keys.length
      ? closed + " question" + (closed === 1 ? "" : "s") + " selected" : "";
  }

  // ---------- quiz flow ----------
  function startQuiz() {
    var keys = selectedKeys();
    var all = poolFor(keys);
    var closed = all.filter(function (q) { return q.type !== "open"; });
    openStudy = all.filter(function (q) { return q.type === "open"; });
    if (!closed.length) return;
    if ($("shuffle").checked) shuffle(closed);
    else closed.sort(function (a, b) {
      return a.exam === b.exam ? a.number - b.number : (a.exam < b.exam ? -1 : 1);
    });
    quiz = closed; idx = 0; answers = []; currentRun = "r" + Date.now();
    show("quiz"); renderQuestion();
  }

  function startRandom() {
    var closed = DATA.questions.filter(function (q) { return q.type !== "open"; });
    shuffle(closed);
    quiz = closed.slice(0, 20);
    openStudy = []; idx = 0; answers = []; currentRun = "r" + Date.now();
    if (!quiz.length) return;
    show("quiz"); renderQuestion();
  }

  function startPractice(scope) {
    var list = buildWeakQuiz(scope);
    if (!list.length) return;
    quiz = list; openStudy = []; idx = 0; answers = []; currentRun = "r" + Date.now();
    show("quiz"); renderQuestion();
  }

  function badgesFor(q) {
    var html = "";
    if (q.exam) html += '<span class="badge year">' + esc(examLabel(q.exam)) + "</span>";
    if (q.generated) html += '<span class="badge ai">AI generated</span>';
    return html;
  }

  function renderQuestion() {
    locked = false;
    var q = quiz[idx];
    $("bar").style.width = (idx / quiz.length * 100) + "%";
    $("qcount").textContent = "Question " + (idx + 1) + " of " + quiz.length;
    var ok = answers.filter(function (a) { return a.correct; }).length;
    $("qscore").textContent = "Score: " + ok + " / " + answers.length;
    $("badges").innerHTML = badgesFor(q);
    $("stem").innerHTML = md(q.stem);

    var opts = $("options");
    opts.innerHTML = "";
    q.options.forEach(function (o, i) {
      var row = document.createElement("label");
      row.className = "opt";
      row.dataset.letter = o.letter;
      row.style.animationDelay = (i * 0.04) + "s";
      row.innerHTML =
        '<input type="checkbox" value="' + o.letter + '">' +
        '<span class="letter">' + o.letter + ")</span>" +
        '<span class="otext">' + inline(o.text) + "</span>" +
        '<span class="mark"></span>';
      var cb = row.querySelector("input");
      cb.addEventListener("change", function () {
        if (locked) return;
        row.classList.toggle("sel", cb.checked);
      });
      opts.appendChild(row);
    });

    var v = $("verdict"); v.className = ""; v.innerHTML = "";
    $("explain").innerHTML = "";
    $("check-btn").classList.remove("hidden");
    $("next-btn").classList.add("hidden");
  }

  function checkAnswer() {
    if (locked) return;
    locked = true;
    var q = quiz[idx];
    var rows = Array.prototype.slice.call($("options").children);
    var selected = rows.filter(function (r) { return r.querySelector("input").checked; })
      .map(function (r) { return r.dataset.letter; });
    var isCorrect = sameSet(selected, q.correct);
    answers.push({ q: q, selected: selected, correct: isCorrect });
    recordAttempt(q.id, isCorrect);

    rows.forEach(function (r) {
      var letter = r.dataset.letter;
      r.classList.add("locked");
      r.querySelector("input").disabled = true;
      var inCorrect = q.correct.indexOf(letter) !== -1;
      var chosen = selected.indexOf(letter) !== -1;
      var mark = r.querySelector(".mark");
      if (inCorrect) { r.classList.add("correct"); mark.textContent = "✓"; }
      else if (chosen) { r.classList.add("wrong"); mark.textContent = "✗"; }
    });

    var v = $("verdict");
    v.className = "verdict " + (isCorrect ? "ok" : "no");
    v.textContent = isCorrect ? "Correct" : "Not quite";
    $("explain").innerHTML = '<div class="expl"><h4>Explanation</h4>' + md(q.explanation) + presChips(q.pdfs) + "</div>";

    $("check-btn").classList.add("hidden");
    var nb = $("next-btn");
    nb.classList.remove("hidden");
    nb.textContent = (idx + 1 >= quiz.length) ? "See results" : "Next";
  }

  function presChips(slugs) {
    if (!slugs || !slugs.length) return "";
    var html = '<div class="chips"><span class="lead">Covered in:</span>';
    slugs.forEach(function (s) {
      var p = PRES[s];
      if (!p) return;
      html += '<a class="chip" target="_blank" rel="noopener" href="../presentation_pdf/' + p.pdf + '.pdf">' + esc(p.name) + "</a>";
    });
    return html + "</div>";
  }

  function next() {
    idx++;
    if (idx >= quiz.length) { renderResults(); return; }
    renderQuestion();
  }

  // ---------- results ----------
  function renderResults() {
    show("results");
    var total = answers.length;
    var correct = answers.filter(function (a) { return a.correct; }).length;
    var pct = total ? Math.round(correct / total * 100) : 0;
    var wrong = answers.filter(function (a) { return !a.correct; });

    var html = "<h2>Results</h2>" +
      '<div class="score">' + pct + '% <small>(' + correct + " / " + total + " correct)</small></div>";

    if (wrong.length) {
      html += "<h2 style='margin-top:24px'>Questions you missed (" + wrong.length + ")</h2>";
      wrong.forEach(function (a) { html += reviewItem(a); });
    } else {
      html += "<p style='margin-top:18px' class='verdict ok'>Perfect — every question correct.</p>";
    }

    if (openStudy.length) {
      html += "<h2 style='margin-top:28px'>Open-ended questions (study)</h2>";
      html += "<p class='empty'>Not scored — read the model answers.</p>";
      openStudy.forEach(function (q) {
        html += '<div class="review-q"><div class="rstem">' + md(q.stem) +
          '<span class="rbadges">' + badgesFor(q) + "</span></div>" +
          '<div class="expl"><h4>Answer</h4>' + md(q.explanation) + presChips(q.pdfs) + "</div></div>";
      });
    }

    html += '<div class="row" style="margin-top:26px;">' +
      '<button id="results-stats" class="ghost">📊 View stats</button><div class="spacer"></div>' +
      '<button id="again-btn" class="primary">Back to start</button></div>';
    $("results").innerHTML = html;
    $("again-btn").addEventListener("click", function () { show("start"); });
    $("results-stats").addEventListener("click", renderStats);
  }

  // ---------- stats screen ----------
  function renderStats() {
    show("stats");
    var ov = overallStat();
    var html = "<h2>Your progress</h2>";
    if (ov.score === null) {
      html += "<p class='empty'>Answer some questions and your per-topic grades (1–10) will appear here. Progress is saved in this browser only.</p>";
      html += statsFooter();
      $("stats").innerHTML = html; wireStats(); return;
    }
    var og = grade10(ov.score);
    html += "<div class='overall'><div class='score " + gradeClass(og) + "'>" + og.toFixed(1) +
      "<small>/10</small></div><div class='ov-meta'>" + Math.round(ov.score * 100) +
      "% recency-weighted · " + ov.tried + " questions practised · " + ov.attempts + " attempts</div></div>";
    html += "<button id='weak-btn' class='weak'>🎯 Practice my weak spots</button>";

    var rows = DATA.topics.map(function (t) {
      var st = topicStat(t.slug);
      return { t: t, st: st, g: grade10(st.score) };
    });
    rows.sort(function (a, b) {
      if (a.st.score === null && b.st.score === null) return a.t.name < b.t.name ? -1 : 1;
      if (a.st.score === null) return 1;
      if (b.st.score === null) return -1;
      return a.st.score - b.st.score;
    });
    html += "<div class='stat-list'>";
    rows.forEach(function (r) {
      var pct = r.st.score === null ? 0 : Math.round(r.st.score * 100);
      var gtxt = r.g === null ? "—" : r.g.toFixed(1);
      html += "<div class='stat-row'>" +
        "<div class='stat-head'><span class='stat-name'>" + esc(r.t.name) + "</span>" +
        "<span class='grade " + gradeClass(r.g) + "'>" + gtxt + "</span></div>" +
        "<div class='stat-bar'><div class='" + gradeClass(r.g) + "' style='width:" + pct + "%'></div></div>" +
        "<div class='stat-sub'><span>" + r.st.tried + "/" + r.st.total + " practised</span>" +
        "<button class='mini' data-topic='" + r.t.slug + "'>Practice</button></div></div>";
    });
    html += "</div>" + statsFooter();
    $("stats").innerHTML = html;
    wireStats();
  }
  function statsFooter() {
    return "<div class='row' style='margin-top:24px;'>" +
      "<button id='reset-btn' class='ghost'>Reset stats</button><div class='spacer'></div>" +
      "<button id='stats-back' class='primary'>Back</button></div>";
  }
  function wireStats() {
    var wb = $("weak-btn"); if (wb) wb.addEventListener("click", function () { startPractice(null); });
    Array.prototype.forEach.call(document.querySelectorAll(".mini[data-topic]"), function (b) {
      b.addEventListener("click", function () { startPractice(b.dataset.topic); });
    });
    $("stats-back").addEventListener("click", function () { show("start"); });
    var rb = $("reset-btn");
    if (rb) rb.addEventListener("click", function () {
      if (confirm("Reset all saved progress on this device?")) { STATS = { questions: {} }; saveStats(); renderStats(); }
    });
  }

  function reviewItem(a) {
    var q = a.q;
    var html = '<div class="review-q"><div class="rstem">' + md(q.stem) +
      '<span class="rbadges">' + badgesFor(q) + "</span></div><ul>";
    q.options.forEach(function (o) {
      var inCorrect = q.correct.indexOf(o.letter) !== -1;
      var chosen = a.selected.indexOf(o.letter) !== -1;
      var cls = inCorrect ? "c" : (chosen ? "x" : "");
      var tag = inCorrect ? " ✓" : (chosen ? " ✗ (your pick)" : "");
      html += '<li class="' + cls + '">' + o.letter + ") " + inline(o.text) + tag + "</li>";
    });
    html += "</ul><div class=\"expl\"><h4>Explanation</h4>" + md(q.explanation) + presChips(q.pdfs) + "</div></div>";
    return html;
  }

  // ---------- screens ----------
  function show(name) {
    ["start", "quiz", "results", "stats"].forEach(function (s) {
      $(s).classList.toggle("hidden", s !== name);
    });
    // retrigger entrance animation
    var el = $(name);
    el.classList.remove("screen"); void el.offsetWidth; el.classList.add("screen");
    window.scrollTo(0, 0);
  }

  // ---------- wire up ----------
  Array.prototype.forEach.call(document.querySelectorAll(".seg-btn"), function (b) {
    b.addEventListener("click", function () {
      document.querySelectorAll(".seg-btn").forEach(function (x) { x.classList.remove("active"); });
      b.classList.add("active");
      mode = b.dataset.mode;
      renderStart();
    });
  });
  $("start-btn").addEventListener("click", startQuiz);
  $("random-btn").addEventListener("click", startRandom);
  $("stats-btn").addEventListener("click", renderStats);
  $("check-btn").addEventListener("click", checkAnswer);
  $("next-btn").addEventListener("click", next);
  $("quit-btn").addEventListener("click", function () {
    if (answers.length && idx + 1 < quiz.length && !confirm("Quit this quiz? Progress will be lost.")) return;
    show("start");
  });

  renderStart();
})();
