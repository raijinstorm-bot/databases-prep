/* Databases exam quiz — vanilla JS, no build step at runtime.
   Data comes from window.QUIZ_DATA (generated by scripts/build.py). */
(function () {
  "use strict";

  var DATA = window.QUIZ_DATA || { exams: [], questions: [], presentations: {} };
  var PRES = DATA.presentations || {};

  var $ = function (id) { return document.getElementById(id); };

  // ---- tiny, safe markdown (escape first, then inline + paragraphs) ----
  function esc(s) {
    return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
  }
  function inline(s) {
    return esc(s)
      .replace(/`([^`]+)`/g, "<code>$1</code>")
      .replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
      .replace(/(^|[^*])\*([^*]+)\*/g, "$1<em>$2</em>");
  }
  function md(s) {
    if (!s) return "";
    return s.split(/\n\s*\n/).map(function (p) {
      return "<p>" + inline(p.trim()).replace(/\n/g, "<br>") + "</p>";
    }).join("");
  }

  function shuffle(a) {
    for (var i = a.length - 1; i > 0; i--) {
      var j = Math.floor(Math.random() * (i + 1));
      var t = a[i]; a[i] = a[j]; a[j] = t;
    }
    return a;
  }
  function sameSet(a, b) {
    if (a.length !== b.length) return false;
    var s = a.slice().sort().join("|");
    return s === b.slice().sort().join("|");
  }

  // ---------- state ----------
  var selectedExams = {};
  var quiz = [];        // closed questions for this run
  var idx = 0;
  var answers = [];     // {q, selected:[], correct:bool}
  var locked = false;

  // ---------- start screen ----------
  function renderStart() {
    var list = $("exam-list");
    list.innerHTML = "";
    if (!DATA.exams.length) {
      list.innerHTML = '<p class="empty">No questions found.</p>';
      return;
    }
    DATA.exams.forEach(function (ex) {
      if (ex.closed === 0 && ex.open === 0) return;
      var div = document.createElement("label");
      div.className = "exam-opt" + (selectedExams[ex.id] ? " sel" : "");
      var meta = ex.closed + " question" + (ex.closed === 1 ? "" : "s");
      if (ex.open) meta += " · " + ex.open + " open";
      div.innerHTML =
        '<input type="checkbox" ' + (selectedExams[ex.id] ? "checked" : "") + '>' +
        '<span class="lbl">' + esc(ex.label) + "</span>" +
        '<span class="meta">' + meta + "</span>";
      var cb = div.querySelector("input");
      cb.addEventListener("change", function () {
        selectedExams[ex.id] = cb.checked;
        div.classList.toggle("sel", cb.checked);
        updateStartBtn();
      });
      list.appendChild(div);
    });
    updateStartBtn();
  }
  function anySelected() {
    return DATA.exams.some(function (e) { return selectedExams[e.id]; });
  }
  function updateStartBtn() {
    $("start-btn").disabled = !anySelected();
  }

  // ---------- quiz flow ----------
  function startQuiz() {
    var chosen = DATA.questions.filter(function (q) {
      return selectedExams[q.exam] && q.type !== "open";
    });
    if ($("shuffle").checked) shuffle(chosen);
    else chosen.sort(function (a, b) {
      return a.exam === b.exam ? a.number - b.number : a.exam < b.exam ? -1 : 1;
    });
    quiz = chosen;
    idx = 0;
    answers = [];
    if (!quiz.length) { return; }
    show("quiz");
    renderQuestion();
  }

  function renderQuestion() {
    locked = false;
    var q = quiz[idx];
    $("bar").style.width = (idx / quiz.length * 100) + "%";
    $("qcount").textContent = "Question " + (idx + 1) + " of " + quiz.length;
    var correctSoFar = answers.filter(function (a) { return a.correct; }).length;
    $("qscore").textContent = "Score: " + correctSoFar + " / " + answers.length;
    $("stem").innerHTML = md(q.stem);

    var opts = $("options");
    opts.innerHTML = "";
    q.options.forEach(function (o) {
      var row = document.createElement("label");
      row.className = "opt";
      row.dataset.letter = o.letter;
      row.innerHTML =
        '<input type="checkbox" value="' + o.letter + '">' +
        '<span class="letter">' + o.letter + ")</span>" +
        '<span class="otext">' + inline(o.text) + "</span>" +
        '<span class="mark"></span>';
      var cb = row.querySelector("input");
      cb.addEventListener("change", function () {
        if (locked) return;
        row.classList.toggle("sel", cb.checked);
      });
      opts.appendChild(row);
    });

    $("verdict").innerHTML = "";
    $("explain").innerHTML = "";
    $("check-btn").classList.remove("hidden");
    $("next-btn").classList.add("hidden");
  }

  function checkAnswer() {
    if (locked) return;
    locked = true;
    var q = quiz[idx];
    var rows = Array.prototype.slice.call($("options").children);
    var selected = rows.filter(function (r) { return r.querySelector("input").checked; })
      .map(function (r) { return r.dataset.letter; });
    var isCorrect = sameSet(selected, q.correct);
    answers.push({ q: q, selected: selected, correct: isCorrect });

    rows.forEach(function (r) {
      var letter = r.dataset.letter;
      r.classList.add("locked");
      r.querySelector("input").disabled = true;
      var inCorrect = q.correct.indexOf(letter) !== -1;
      var chosen = selected.indexOf(letter) !== -1;
      var mark = r.querySelector(".mark");
      if (inCorrect) { r.classList.add("correct"); mark.textContent = "✓"; }
      else if (chosen) { r.classList.add("wrong"); mark.textContent = "✗"; }
    });

    var v = $("verdict");
    v.className = "verdict " + (isCorrect ? "ok" : "no");
    v.textContent = isCorrect ? "Correct" : "Not quite";

    $("explain").innerHTML =
      '<div class="expl"><h4>Explanation</h4>' + md(q.explanation) + presChips(q.pdfs) + "</div>";

    $("check-btn").classList.add("hidden");
    var nb = $("next-btn");
    nb.classList.remove("hidden");
    nb.textContent = (idx + 1 >= quiz.length) ? "See results" : "Next";
  }

  function presChips(slugs) {
    if (!slugs || !slugs.length) return "";
    var html = '<div class="chips"><span class="lead">Covered in:</span>';
    slugs.forEach(function (s) {
      var p = PRES[s];
      if (!p) return;
      html += '<a class="chip" target="_blank" rel="noopener" href="../presentation_pdf/' +
        p.pdf + '.pdf">' + esc(p.name) + "</a>";
    });
    return html + "</div>";
  }

  function next() {
    idx++;
    if (idx >= quiz.length) { renderResults(); return; }
    renderQuestion();
  }

  // ---------- results ----------
  function renderResults() {
    show("results");
    var total = answers.length;
    var correct = answers.filter(function (a) { return a.correct; }).length;
    var pct = total ? Math.round(correct / total * 100) : 0;
    var wrong = answers.filter(function (a) { return !a.correct; });

    var html = "<h2>Results</h2>" +
      '<div class="score">' + pct + '% <small>(' + correct + " / " + total + " correct)</small></div>";

    if (wrong.length) {
      html += "<h2 style='margin-top:24px'>Questions you missed (" + wrong.length + ")</h2>";
      wrong.forEach(function (a) {
        html += reviewItem(a);
      });
    } else {
      html += "<p style='margin-top:18px' class='verdict ok'>Perfect — every question correct.</p>";
    }

    // open questions for the chosen exams (study, not scored)
    var openQs = DATA.questions.filter(function (q) {
      return selectedExams[q.exam] && q.type === "open";
    });
    if (openQs.length) {
      html += "<h2 style='margin-top:28px'>Open-ended questions (study)</h2>";
      html += "<p class='empty'>These are not scored. Read the model answers.</p>";
      openQs.forEach(function (q) {
        html += '<div class="review-q"><div class="rstem">' + md(q.stem) +
          '<span class="tag-open">' + esc(examLabel(q.exam)) + "</span></div>" +
          '<div class="expl"><h4>Answer</h4>' + md(q.explanation) + presChips(q.pdfs) + "</div></div>";
      });
    }

    html += '<div class="row" style="margin-top:26px;"><div class="spacer"></div>' +
      '<button id="again-btn" class="primary">Back to start</button></div>';

    $("results").innerHTML = html;
    $("again-btn").addEventListener("click", function () {
      show("start");
    });
  }

  function reviewItem(a) {
    var q = a.q;
    var html = '<div class="review-q"><div class="rstem">' + md(q.stem) +
      '<span class="tag-open">' + esc(examLabel(q.exam)) + "</span></div><ul>";
    q.options.forEach(function (o) {
      var inCorrect = q.correct.indexOf(o.letter) !== -1;
      var chosen = a.selected.indexOf(o.letter) !== -1;
      var cls = inCorrect ? "c" : (chosen ? "x" : "");
      var tag = inCorrect ? " ✓" : (chosen ? " ✗ (your pick)" : "");
      html += '<li class="' + cls + '">' + o.letter + ") " + inline(o.text) + tag + "</li>";
    });
    html += "</ul>";
    html += '<div class="expl"><h4>Explanation</h4>' + md(q.explanation) + presChips(q.pdfs) + "</div></div>";
    return html;
  }

  function examLabel(id) {
    var e = DATA.exams.filter(function (x) { return x.id === id; })[0];
    return e ? e.label : id;
  }

  // ---------- screens ----------
  function show(name) {
    ["start", "quiz", "results"].forEach(function (s) {
      $(s).classList.toggle("hidden", s !== name);
    });
    window.scrollTo(0, 0);
  }

  // ---------- wire up ----------
  $("start-btn").addEventListener("click", startQuiz);
  $("check-btn").addEventListener("click", checkAnswer);
  $("next-btn").addEventListener("click", next);
  $("quit-btn").addEventListener("click", function () {
    if (answers.length && idx + 1 < quiz.length && !confirm("Quit this quiz? Progress will be lost.")) return;
    show("start");
  });

  renderStart();
})();
